    private int camposVacios(){
        
        int res = 0;
        
        if(this.Nombre.getText().trim().length() == 0 ||
           this.ApellidoPaterno.getText().trim().length() == 0 ||
           this.ApellidoMaterno.getText().trim().length() == 0 ||
           this.Direccion.getText().trim().length() == 0 ||
           this.CP.getText().trim().length() == 0 ||
           this.Telefono.getText().trim().length() == 0 ||
           this.Ciudad.getText().trim().length() == 0 ||
           this.Email.getText().trim().length() == 0)
           res = 1;
        
        return(res);
    }
    
