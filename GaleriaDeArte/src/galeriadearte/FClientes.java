    /**
     * Método para dar de baja un cliente de la base de datos
     */
    public void DeleteClient(){
        int opc;
        
        //Se checa que se haya seleccionado un registro de algun cliente.
        if(TablaClientes.getSelectedRow() >= 0)
        {
            opc = JOptionPane.showConfirmDialog(null, "¿Esta seguro que quiere eliminar el registro?", "Eliminación", JOptionPane.YES_NO_OPTION);
            if(opc == 0)
            {
                //Se prepara la consulta de eleminación y despues es ejecutada
                sql = "delete from VENTAS.Clientes where Id_Cliente = "+this.IdCliente.getText();
                EjecutaSentencia(TablaClientes);
            }
        }else {
            JOptionPane.showMessageDialog(null, "Seleccione un registro","Eliminación", JOptionPane.ERROR_MESSAGE);
        }
    }    
