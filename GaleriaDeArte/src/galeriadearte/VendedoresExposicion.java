    /**
     * Este método se encarga de cargar los nombres de los vendedores en un comboBox y a su vez su llave
     * primaria almacenandola en un arreglo
     */
    public void CargaVendedores(){
        
        this.numArtistas = 0;
        
        try
        {
             con.EstableceConexion();
             rsArtistas = con.getSentencia().executeQuery("select V.Id_Vendedor, (V.Nombre||' '||V.Apellido_Paterno) AS Nombre  from VENTAS.Vendedores V");
    
             while(rsArtistas.next())
             {
               cbVendedores.addItem(rsArtistas.getObject(2));
               Vendedores[numArtistas++] = Integer.parseInt( String.valueOf(rsArtistas.getObject(1)));
             }
             
             con.cierraConexion();
             
        }catch(Exception e)
        {
           JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
   
