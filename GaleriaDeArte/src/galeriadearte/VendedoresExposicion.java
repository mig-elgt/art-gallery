    /**
     * Método para eleminar un vendedor en alguna exposición. La eleminación es de manera 
     * fisica y lógica.
     */
    public void DeleteVendedorExp(){
        int opc;
        int fila=this.VendedoresExposicion.getSelectedRow();
        
        //Se obtiene la llave primaria de la exposición.
        long idV=Long.parseLong(String.valueOf(this.VendedoresExposicion.getModel().getValueAt(fila, 1)));
        
        if(VendedoresExposicion.getSelectedRow() >= 0)
        {
            opc = JOptionPane.showConfirmDialog(null, "¿Esta seguro que quiere eliminar el registro?", "Eliminación", JOptionPane.YES_NO_OPTION);
            if(opc == 0)
            {
                sql = "delete from VENTAS.comisionesporexposicion where codigo_exposicion = "+this.etIdExp.getText()
                       + "and codigo_vendedor = "+idV ;
                con.setSQL(sql);
                con.setQueryTabla("select * from VENTAS.comisionesporexposicion C where C.codigo_exposicion="+this.id);
                con.EjecutaSentencia();
            }
        }else {
            JOptionPane.showMessageDialog(null, "Seleccione un registro","Eliminación", JOptionPane.ERROR_MESSAGE);
        }
    }
    
