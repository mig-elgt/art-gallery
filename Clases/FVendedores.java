    public void DeleteVendedor(){
        int opc;
        
        //Se verifica que se haya seleccionado el registro del vendedor
        if(TablaVendedores.getSelectedRow() >= 0)
        {
            opc = JOptionPane.showConfirmDialog(null, "¿Esta seguro que quiere eliminar el registro?", "Eliminación", JOptionPane.YES_NO_OPTION);
            if(opc == 0)
            {
                sql = "delete from VENTAS.Vendedores where Id_Vendedor = "+this.IdVendedor.getText();
                EjecutaSentencia(TablaVendedores);
            }
        }else {
            JOptionPane.showMessageDialog(null, "Seleccione un registro","Eliminación", JOptionPane.ERROR_MESSAGE);
        }
    }
    
