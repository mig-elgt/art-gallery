    private void validaDigito(java.awt.event.KeyEvent evt, int l, JTextField t){
        
        if((t.getText().length() >= l) || (!Character.isDigit(evt.getKeyChar()) && !Character.isISOControl(evt.getKeyChar())))
        {
            Toolkit.getDefaultToolkit().beep();
            evt.consume();
        }
    }
   private void validaCadena(java.awt.event.KeyEvent evt){

        if(!Character.isSpaceChar(evt.getKeyChar()) && !Character.isISOControl(evt.getKeyChar()) &&!Character.isLetter(evt.getKeyChar()))
        { 
            Toolkit.getDefaultToolkit().beep();
            evt.consume();
        }
    }
  
